export function checkFullName(name){
    return (name && name.split(" ")[1] && name.split(" ")[0][0] === name.split(" ")[0][0].toUpperCase() && name.split(" ")[1][0] === name.split(" ")[1][0].toUpperCase());
}

export function isMobile(){
    return window.innerWidth <= 760;
}

export function isDesktop(){
    return window.innerWidth > 1200;
}

export function isEmail(email){
    const re = /^(([^<>()\[\]\\.,;:\s@"]+(\.[^<>()\[\]\\.,;:\s@"]+)*)|(".+"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/;

    return re.test(String(email).toLowerCase());
}

export function equals(one, two){
    return one === two;
}

export function newArray(arrayLength, parameters){
    return new Array(arrayLength).fill().map((e,i) => {
            return {
                id: i,
                ...parameters
            }
        }
    )
}
